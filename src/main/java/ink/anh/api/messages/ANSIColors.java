package ink.anh.api.messages;

/**
 * Provides ANSI color codes for use in console messages.
 * These colors can be used to enhance the readability and visual appeal of console logs.
 */
public class ANSIColors {
    /** ANSI escape code for black text. */
    public static final String BLACK = "\u001B[30m";

    /** ANSI escape code for red text. */
    public static final String RED = "\u001B[31m";

    /** ANSI escape code for green text. */
    public static final String GREEN = "\u001B[32m";

    /** ANSI escape code for yellow text. */
    public static final String YELLOW = "\u001B[33m";

    /** ANSI escape code for blue text. */
    public static final String BLUE = "\u001B[34m";

    /** ANSI escape code for purple text. */
    public static final String PURPLE = "\u001B[35m";

    /** ANSI escape code for cyan text. */
    public static final String CYAN = "\u001B[36m";

    /** ANSI escape code for white text. */
    public static final String WHITE = "\u001B[37m";

    /** ANSI escape code for bright black text. */
    public static final String BLACK_BRIGHT = "\u001B[90m";

    /** ANSI escape code for bright red text. */
    public static final String RED_BRIGHT = "\u001B[91m";

    /** ANSI escape code for bright green text. */
    public static final String GREEN_BRIGHT = "\u001B[92m";

    /** ANSI escape code for bright yellow text. */
    public static final String YELLOW_BRIGHT = "\u001B[93m";

    /** ANSI escape code for bright blue text. */
    public static final String BLUE_BRIGHT = "\u001B[94m";

    /** ANSI escape code for bright purple text. */
    public static final String PURPLE_BRIGHT = "\u001B[95m";

    /** ANSI escape code for bright cyan text. */
    public static final String CYAN_BRIGHT = "\u001B[96m";

    /** ANSI escape code for bright white text. */
    public static final String WHITE_BRIGHT = "\u001B[97m";

    /** ANSI escape code to reset text color to default. */
    public static final String RESET = "\u001B[0m";
}
